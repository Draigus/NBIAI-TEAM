#!/usr/bin/env node
'use strict';
// Tests Phase 7: memory conflict handling.
// Run: node .claude/harness/tests/test-memory-conflict.js

const fs = require('fs');
const os = require('os');
const path = require('path');

let passed = 0;
let failed = 0;

function assert(cond, msg) {
  if (cond) { passed++; console.log('  PASS: ' + msg); }
  else { failed++; console.error('  FAIL: ' + msg); }
}

function assertEq(actual, expected, msg) {
  if (actual === expected) { passed++; console.log('  PASS: ' + msg); }
  else { failed++; console.error('  FAIL: ' + msg + ' — expected ' + JSON.stringify(expected) + ', got ' + JSON.stringify(actual)); }
}

// --- Setup temp memory dir ---
const tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), 'rho-memconflict-'));
const memoryDir = path.join(tmpDir, 'memory');
fs.mkdirSync(memoryDir, { recursive: true });

process.env.CLAUDE_PROJECT_DIR = tmpDir;
process.env.HARNESS_DIR = path.join(tmpDir, '.claude', 'harness');
delete require.cache[require.resolve('../lib/resolve.js')];
delete require.cache[require.resolve('../lib/memory-conflict.js')];
const mc = require('../lib/memory-conflict.js');

// Write Glen-explicit memory files
fs.writeFileSync(path.join(memoryDir, 'feedback_verify_work.md'),
  '---\nname: verify-work\ndescription: HARD RULE verify all work end-to-end before claiming done\nmetadata:\n  type: feedback\n---\nAlways verify work.\n');

fs.writeFileSync(path.join(memoryDir, 'feedback_no_corner_cutting.md'),
  '---\nname: no-corner-cutting\ndescription: HARD RULE never declare done without reading agent outputs\nmetadata:\n  type: feedback\n---\nNo shortcuts.\n');

fs.writeFileSync(path.join(memoryDir, 'user_glen.md'),
  '---\nname: glen-profile\ndescription: Glen Pryer profile and preferences\nmetadata:\n  type: user\n---\nGlen is the MD.\n');

// Write harness-generated memory
fs.writeFileSync(path.join(memoryDir, 'feedback_harness_test.md'),
  '---\nname: harness-test-rule\ndescription: Auto-generated test verification rule\nsource: harness_rho\nauto_generated: true\nmetadata:\n  type: feedback\n---\nGenerated by harness.\n');

// Write MEMORY.md (should be excluded from scanning)
fs.writeFileSync(path.join(memoryDir, 'MEMORY.md'),
  '# Memory Index\n- [Verify Work](feedback_verify_work.md)\n');

// ===== parseFrontmatter =====

console.log('\nTest: parseFrontmatter\n');

const fm1 = mc.parseFrontmatter('---\nname: test-name\ndescription: A test description\n---\nBody content');
assert(fm1 !== null, 'parseFrontmatter returns non-null for valid frontmatter');
assertEq(fm1.name, 'test-name', 'name extracted');
assertEq(fm1.description, 'A test description', 'description extracted');

const fm2 = mc.parseFrontmatter('---\nname: bool-test\nauto_generated: true\ncount: 42\n---\n');
assertEq(fm2.auto_generated, true, 'boolean true parsed');
assertEq(fm2.count, 42, 'integer parsed');

const fm3 = mc.parseFrontmatter('No frontmatter here');
assertEq(fm3, null, 'parseFrontmatter returns null when no frontmatter');

assertEq(mc.parseFrontmatter(null), null, 'parseFrontmatter handles null');
assertEq(mc.parseFrontmatter(''), null, 'parseFrontmatter handles empty string');

// Nested metadata
const fm4 = mc.parseFrontmatter('---\nname: nested-test\nmetadata:\n  type: feedback\n---\n');
assert(fm4.metadata !== undefined, 'nested metadata parsed');
assertEq(fm4.metadata.type, 'feedback', 'nested metadata.type extracted');

// ===== parseYamlValue =====

console.log('\n--- parseYamlValue ---');

assertEq(mc.parseYamlValue('true'), true, 'true parsed as boolean');
assertEq(mc.parseYamlValue('false'), false, 'false parsed as boolean');
assertEq(mc.parseYamlValue('null'), null, 'null parsed as null');
assertEq(mc.parseYamlValue(''), null, 'empty string parsed as null');
assertEq(mc.parseYamlValue('42'), 42, 'integer parsed');
assertEq(mc.parseYamlValue('3.14'), 3.14, 'float parsed');
assertEq(mc.parseYamlValue('hello'), 'hello', 'string preserved');
assertEq(mc.parseYamlValue('"quoted"'), 'quoted', 'quoted string unquoted');

// ===== slugMatch =====

console.log('\n--- slugMatch ---');

assert(mc.slugMatch('verify-work', 'verify-work'), 'exact slug match');
assert(mc.slugMatch('Verify-Work', 'verify-work'), 'case-insensitive slug match');
assert(!mc.slugMatch('verify-work', 'no-corner-cutting'), 'different slugs do not match');
assert(!mc.slugMatch('', 'verify-work'), 'empty slug does not match');
assert(!mc.slugMatch(null, 'verify-work'), 'null slug does not match');

// ===== descriptionOverlap =====

console.log('\n--- descriptionOverlap ---');

assert(mc.descriptionOverlap(
  'HARD RULE verify all work end-to-end',
  'verify work end-to-end before completion'
), 'overlapping descriptions detected');

assert(!mc.descriptionOverlap(
  'HARD RULE verify all work end-to-end',
  'Glen Pryer profile and preferences'
), 'non-overlapping descriptions do not match');

assert(!mc.descriptionOverlap('', 'something'), 'empty first desc no overlap');
assert(!mc.descriptionOverlap(null, 'something'), 'null first desc no overlap');

// ===== extractKeywords =====

console.log('\n--- extractKeywords ---');

const kw = mc.extractKeywords('hard rule verify all work end-to-end');
assert(kw.indexOf('hard') !== -1, 'hard extracted');
assert(kw.indexOf('rule') !== -1, 'rule extracted');
assert(kw.indexOf('verify') !== -1, 'verify extracted');
assert(kw.indexOf('work') !== -1, 'work extracted');
assert(kw.indexOf('end-to-end') !== -1, 'end-to-end extracted');
assert(kw.indexOf('all') === -1, 'stop word "all" excluded');

const kw2 = mc.extractKeywords('');
assertEq(kw2.length, 0, 'empty string produces no keywords');

// ===== isMemoryPath =====

console.log('\n--- isMemoryPath ---');

assert(mc.isMemoryPath('memory/feedback_test.md'), 'memory path detected');
assert(mc.isMemoryPath('d:\\project\\memory\\user_glen.md'), 'Windows memory path detected');
assert(!mc.isMemoryPath('roles/senior_engineer/AGENT.md'), 'non-memory path rejected');
assert(mc.isMemoryPath('memory/MEMORY.md'), 'MEMORY.md passes isMemoryPath (excluded by listMemoryFiles instead)');
assert(!mc.isMemoryPath(''), 'empty path rejected');

// ===== extractSlugFromPath =====

console.log('\n--- extractSlugFromPath ---');

assertEq(mc.extractSlugFromPath('memory/feedback_verify_work.md'), 'feedback_verify_work', 'slug from memory path');
assertEq(mc.extractSlugFromPath('d:\\project\\memory\\user_glen.md'), 'user_glen', 'slug from Windows path');
assertEq(mc.extractSlugFromPath('roles/x/AGENT.md'), null, 'null for non-memory path');

// ===== extractSlugFromContent =====

console.log('\n--- extractSlugFromContent ---');

assertEq(mc.extractSlugFromContent('name: test-slug\ndescription: blah'), 'test-slug', 'slug from content');
assertEq(mc.extractSlugFromContent('no name here'), null, 'null when no name line');
assertEq(mc.extractSlugFromContent(null), null, 'null for null content');

// ===== findConflicts =====

console.log('\n--- findConflicts ---');

// Proposal targeting same slug as Glen memory
const slugConflictProposal = {
  target_file: 'memory/feedback_verify_work.md',
  diagnosis: 'some new verification rule'
};
const slugConflicts = mc.findConflicts(slugConflictProposal, memoryDir);
assert(slugConflicts.length >= 1, 'same-file conflict found');
assertEq(slugConflicts[0].match_type, 'same_file', 'conflict type is same_file');
assertEq(slugConflicts[0].memory_name, 'verify-work', 'conflicting memory name identified');
assert(slugConflicts[0].is_glen_explicit, 'conflicting memory is Glen-explicit');

// Proposal targeting harness-generated memory — no conflict
const harnessProposal = {
  target_file: 'memory/feedback_harness_test.md',
  diagnosis: 'update harness test rule'
};
const harnessConflicts = mc.findConflicts(harnessProposal, memoryDir);
assertEq(harnessConflicts.length, 0, 'no conflict with harness-generated memory');

// Proposal with description overlap
const descOverlapProposal = {
  target_file: 'memory/feedback_new_verify.md',
  diagnosis: 'HARD RULE verify all work end-to-end before claiming done'
};
const descConflicts = mc.findConflicts(descOverlapProposal, memoryDir);
assert(descConflicts.length >= 1, 'description overlap conflict found');
var hasDescOverlap = descConflicts.some(function(c) { return c.match_type === 'description_overlap'; });
assert(hasDescOverlap, 'at least one conflict is description_overlap');

// Proposal with content frontmatter name matching Glen memory
const contentSlugProposal = {
  target_file: 'memory/feedback_new_file.md',
  content: '---\nname: verify-work\ndescription: new verification rule\n---\nNew content.',
  diagnosis: 'unrelated diagnosis text'
};
const contentSlugConflicts = mc.findConflicts(contentSlugProposal, memoryDir);
assert(contentSlugConflicts.length >= 1, 'content frontmatter slug conflict found');
var hasSlugFromContent = contentSlugConflicts.some(function(c) { return c.match_type === 'slug_match'; });
assert(hasSlugFromContent, 'conflict detected via content frontmatter name');

// Non-memory target — no conflicts
const nonMemoryProposal = {
  target_file: 'roles/senior_engineer/AGENT.md',
  diagnosis: 'add knowledge section'
};
assertEq(mc.findConflicts(nonMemoryProposal, memoryDir).length, 0, 'non-memory target produces no conflicts');

// Null proposal
assertEq(mc.findConflicts(null, memoryDir).length, 0, 'null proposal produces no conflicts');

// ===== promoteRisk =====

console.log('\n--- promoteRisk ---');

const lowProposal = { risk: 'LOW', target_file: 'memory/test.md' };
const promoted = mc.promoteRisk(lowProposal, [
  { memory_name: 'verify-work', is_glen_explicit: true }
]);
assertEq(promoted.risk, 'HIGH', 'LOW promoted to HIGH on Glen conflict');
assert(promoted.conflict_with.indexOf('verify-work') !== -1, 'conflict_with set');
assert(promoted.promotion_reason.includes('verify-work'), 'promotion_reason mentions conflicting memory');

// Already HIGH — stays HIGH
const highProposal = { risk: 'HIGH', target_file: 'memory/test.md' };
const staysHigh = mc.promoteRisk(highProposal, [
  { memory_name: 'glen-profile', is_glen_explicit: true }
]);
assertEq(staysHigh.risk, 'HIGH', 'HIGH stays HIGH');
assert(staysHigh.conflict_with.indexOf('glen-profile') !== -1, 'conflict_with set for HIGH');

// No Glen conflicts — no promotion
const noGlenConflict = mc.promoteRisk(lowProposal, [
  { memory_name: 'harness-rule', is_glen_explicit: false }
]);
assertEq(noGlenConflict.risk, 'LOW', 'LOW stays LOW when no Glen conflicts');

// Empty conflicts — no change
const noConflicts = mc.promoteRisk(lowProposal, []);
assertEq(noConflicts.risk, 'LOW', 'no conflicts means no promotion');

// ===== checkProposal =====

console.log('\n--- checkProposal ---');

const checkResult = mc.checkProposal(slugConflictProposal, memoryDir);
assertEq(checkResult.hasConflict, true, 'checkProposal detects conflict');
assert(checkResult.conflicts.length >= 1, 'checkProposal returns conflicts');

const noConflictResult = mc.checkProposal(nonMemoryProposal, memoryDir);
assertEq(noConflictResult.hasConflict, false, 'checkProposal returns no conflict for non-memory target');

// ===== listMemoryFiles =====

console.log('\n--- listMemoryFiles ---');

const memFiles = mc.listMemoryFiles(memoryDir);
assert(memFiles.length >= 4, 'listMemoryFiles finds memory files');
assert(!memFiles.some(function(f) { return f.endsWith('MEMORY.md'); }), 'MEMORY.md excluded');

// Non-existent dir
assertEq(mc.listMemoryFiles('/nonexistent/path').length, 0, 'nonexistent dir returns empty');

// ===== Cleanup =====
fs.rmSync(tmpDir, { recursive: true, force: true });

// ===== Summary =====
console.log('\n' + passed + ' passed, ' + failed + ' failed');
process.exit(failed > 0 ? 1 : 0);
